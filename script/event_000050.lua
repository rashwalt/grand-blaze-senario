マルンベイム平原の小高い丘に、数人の人影があった。

小高い丘からは、様々な種族、性別、装備をした人々の陰が広がっている。
その姿は千差万別。統一性はなく、がやがやとしていた。

「諸君！」

ひときわ大きい声があたりに響く。
その声に、今まで話していた人々の声が止まる。
……その声は、誰もが、聴いたことのある声だったから。

「突然の招集、突然のミッション発行に驚いたことだろう！
　諸君に集まってもらったのは他でもない。
　今、世界は危機に瀕している！」

その言葉に、再び、がやがやと騒がしくなる。

　世界は危機に瀕しているだって……？

　魔神戦争が再来するというのか…？

　もっと、何か別のものかしら…？

聴衆が落ち着くのを待ってから、再び、彼は口を開く。

「君たちの中には、こんな経験をした者がいないだろうか！
　もう一人の自分が、強大な敵と戦い、世界が白い光に包まれた……。
　そんな、自分では体験したことのないはずの出来事を『憶えている』ことが！」

　そういえば、そんな記憶がなぜか憶えているんだよな……。

　あれ、あんたも？

　それは記憶違いってやつだろ？　ほら、デジャヴみたいな……。

「そして、君たちの中には、こんな理論を聞いたことはないだろうか！
　『世界には、平行した別の世界がある』と！」

　知っているぜ、たしか、えーと、平行世界、だっけ？

「……もし、それらが真実だとし……。
　もう一つの世界を滅ぼした元凶が今再び、この世界へやってきていると聞いたら、諸君は信じてくれるであろうか。
