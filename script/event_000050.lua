　そして、今各国の状況は、あまりいい状況とはいえない。
　帝国の動きもそうだが……下手に動けば、侵略行為と受け取られかねない状況だ。
　……マークフリック殿が動いてくれてはいるが、な……」

グランドギルドマスター・ジム
「まったく、お国の連中は、これだから。
　今は世界の危機だってのに、まだそんなこと言ってんのか」

ケーンリッジ元帥
「それが、国家というものだよ。
　……あの作戦を大統領が下せたのも、マークフリック殿と、そして……彼女の力があってこそだ。
　そうでなければ、身動きはできなかっただろうな」

グランドギルドマスター・ジム
「一体、いつになったら、『同盟』は実現するのやら。
　それで、うちの連中に何かあったら、俺は許さねぇからな」

ジムはそういいながら、丘の下にいる冒険者たちを眺める。

ケーンリッジ元帥
「……すまん」

そんなジムに、ケーンリッジは素直に頭を下げた。

グランドギルドマスター・ジム
「……まあ、ともかく。
　早く本当の『オペレーション・インディペンデンス』を発動させてくれ。
　……奴の強さは、うちの連中がいくら勇猛果敢でも、太刀打ちできるかどうか……」

ケーンリッジ元帥
「……ジム。
　ここ最近、夢で見るあの光景は、あまりうれしい状況ではないからな。
　少女の祈りが世界を白く染めた、そこで目が覚めるのだよ。
　ただの夢だろうと思っていた。
　だが、実際にあの少女が目の前に現れたとき、何か重大な事実を見落としている気がした。
　『独立』できるかどうかは、冒険者たちの手にかかっている。
　それは間違いない…。彼らはいわゆるイレギュラーだ。
　もちろん、世界を構成する一人一人の人間だが、その行動は、『束縛』から『独立』するためのカギのひとつだと思うよ。
　……必ず、間に合わせる。
　我々国家、いや、人間の底力を、奴に見せてやろう」

グランドギルドマスター・ジム
「頼むぜ、大将。
　『ホワイトスフィア』を見つけ出してくれ」

ケーンリッジ元帥
「わかっている……！」

ケーンリッジが立ち去ると、ジムは冒険者たちを見回した。
一人一人の顔を、改めて記憶に刻むかのように。

