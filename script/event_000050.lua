その声に、冒険者たちが声を振り上げる。

大歓声は、平原を揺るがすかのようだった。

軍服の男性
「……まったく、貴殿ももう若くはないのだ。
　もう少し、落ち着いたらどうだね」

丘の上で、微笑を浮かべながら、軍服の男性が言う。

グランドギルドマスター・ジム
「へっ、悪いな。
　堅苦しいのはやっぱ俺には無理なんでな。
　そっちこそ、連邦軍の準備はできたのか？
　ケーンリッジ」

ケーンリッジ元帥
「大統領はすでに海兵隊に『オペレーション・インディペンデンス』の発令を下した。
　……だが、表だって全軍が動くことはできないな。
　敵はこのファーネルだけではない。世界に対して牙をむいている。
